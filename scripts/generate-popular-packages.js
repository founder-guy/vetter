#!/usr/bin/env node
/**
 * Generate popular-packages.ts from all.json
 * Extracts top 1000 packages with tier information
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// Read the all.json file
const allJsonPath = path.join(__dirname, '..', 'all.json');
const outputPath = path.join(__dirname, '..', 'src', 'data', 'popular-packages.ts');

console.log('Reading all.json...');
const rawData = fs.readFileSync(allJsonPath, 'utf-8');
const allPackages = JSON.parse(rawData);

console.log(`Found ${allPackages.length} total packages`);

// Extract top 1000 with tier information
const top1000 = allPackages.slice(0, 1000).map(([name, downloads], index) => {
  let tier;
  if (index < 100) {
    tier = 'top-100';
  } else if (index < 500) {
    tier = 'top-500';
  } else {
    tier = 'top-1000';
  }
  return { name, tier, downloads };
});

console.log(`Extracted top 1000 packages`);
console.log(`Top 100: ${top1000.filter(p => p.tier === 'top-100').length}`);
console.log(`Top 500: ${top1000.filter(p => p.tier === 'top-500').length}`);
console.log(`Top 1000: ${top1000.filter(p => p.tier === 'top-1000').length}`);

// Generate TypeScript file
const tsContent = `// Auto-generated by scripts/generate-popular-packages.js
// Last updated: ${new Date().toISOString()}
// Data source: https://github.com/evanwashere/top-npm-packages
//
// This file contains the top 1000 most downloaded npm packages
// Used for typosquatting detection

export interface PopularPackage {
  name: string;
  tier: 'top-100' | 'top-500' | 'top-1000';
}

export const POPULAR_PACKAGES: PopularPackage[] = [
${top1000.map(p => `  { name: '${p.name}', tier: '${p.tier}' },`).join('\n')}
];

// Pre-computed sets for O(1) lookups
export const TOP_100_SET = new Set(
  POPULAR_PACKAGES.filter(p => p.tier === 'top-100').map(p => p.name)
);

export const TOP_500_SET = new Set(
  POPULAR_PACKAGES.filter(p => p.tier === 'top-100' || p.tier === 'top-500').map(p => p.name)
);

export const TOP_1000_SET = new Set(POPULAR_PACKAGES.map(p => p.name));
`;

// Ensure src/data directory exists
const dataDir = path.dirname(outputPath);
if (!fs.existsSync(dataDir)) {
  fs.mkdirSync(dataDir, { recursive: true });
  console.log(`Created directory: ${dataDir}`);
}

// Write TypeScript file
fs.writeFileSync(outputPath, tsContent, 'utf-8');

console.log(`✓ Generated ${outputPath}`);
console.log(`  File size: ${(fs.statSync(outputPath).size / 1024).toFixed(2)} KB`);
console.log(`\nTop 10 packages:`);
top1000.slice(0, 10).forEach((p, i) => {
  console.log(`  ${i + 1}. ${p.name} (${p.downloads.toLocaleString()} downloads/month)`);
});
